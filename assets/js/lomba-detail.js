document.addEventListener('DOMContentLoaded', function () {
  // Ambil parameter lomba dari URL
  const params = new URLSearchParams(window.location.search);
  const key = params.get('lomba');

  if (!key || !LOMBA_DATA[key]) {
    // Redirect jika parameter tidak valid
    window.location.href = "acara.html";
    return;
  }

  const data = LOMBA_DATA[key];

  // Render judul dan breadcrumb
  document.getElementById('compTitle').innerText = data.title;
  document.getElementById('compBreadcrumb').innerText = data.title;
  document.getElementById('compIcon').className = data.icon;
  document.getElementById('compIcon').style.color = 'var(--color-hijau-muda)';

  // Render tema dan deskripsi
  document.getElementById('compTheme').innerText = `“${data.theme}”`;
  document.getElementById('compDesc').innerText = data.description;

  // Render info HTM
  document.getElementById('htmBatch1').innerText = data.htm.batch1;
  document.getElementById('htmBatch2').innerText = data.htm.batch2;

  // Render sub-tema jika ada
  const subThemesSection = document.getElementById('subThemesSection');
  if (data.subTema) {
    subThemesSection.style.display = "block";
    const subList = document.getElementById('subThemesList');
    subList.innerHTML = "";
    data.subTema.forEach(st => {
      const li = document.createElement('li');
      li.className = "mb-2";
      li.style.color = "rgba(255,255,255,0.7)";
      li.innerHTML = `<i class="ri-checkbox-circle-fill text-accent me-2"></i> ${st}`;
      subList.appendChild(li);
    });
  } else {
    subThemesSection.style.display = "none";
  }

  // Render timeline
  const timelineBody = document.getElementById('timelineBody');
  timelineBody.innerHTML = "";
  data.timeline.forEach(item => {
    const row = document.createElement('div');
    row.className = 'timeline-row';
    row.innerHTML = `
      <span class="timeline-event">${item.event}</span>
      <span class="timeline-date">${item.date}</span>
    `;
    timelineBody.appendChild(row);
  });

  // Render syarat dan ketentuan
  const syaratList = document.getElementById('syaratList');
  syaratList.innerHTML = "";
  data.syarat.forEach(s => {
    const li = document.createElement('li');
    li.className = 'detail-list-item';
    li.innerHTML = `<i class="ri-checkbox-circle-line"></i> <span>${s}</span>`;
    syaratList.appendChild(li);
  });

  // Render ketentuan karya
  const ketentuanList = document.getElementById('ketentuanList');
  ketentuanList.innerHTML = "";
  data.ketentuan.forEach(k => {
    const li = document.createElement('li');
    li.className = 'detail-list-item';
    li.innerHTML = `<i class="ri-article-line"></i> <span>${k}</span>`;
    ketentuanList.appendChild(li);
  });

  // Render mekanisme pengumpulan
  const pengumpulanSection = document.getElementById('pengumpulanSection');
  if (data.pengumpulan) {
    pengumpulanSection.style.display = "block";
    const pengumpulanList = document.getElementById('pengumpulanList');
    pengumpulanList.innerHTML = "";
    data.pengumpulan.forEach(p => {
      const li = document.createElement('li');
      li.className = 'detail-list-item';
      li.innerHTML = `<i class="ri-upload-cloud-2-line"></i> <span>${p}</span>`;
      pengumpulanList.appendChild(li);
    });
  } else {
    pengumpulanSection.style.display = "none";
  }

  // Render kriteria penilaian
  const criteriaBody = document.getElementById('criteriaBody');
  criteriaBody.innerHTML = "";
  data.criteriaHeader = (key === "puisi" || key === "poster-digital") ? "Persentase" : "Poin";
  document.getElementById('criteriaHeaderLabel').innerText = data.criteriaHeader;
  data.kriteria.forEach(c => {
    const tr = document.createElement('tr');
    tr.innerHTML = `
      <td>${c.aspek}</td>
      <td class="poin-cell">${c.poin}</td>
    `;
    criteriaBody.appendChild(tr);
  });

  // Render hadiah
  const hadiahSection = document.getElementById('hadiahSection');
  if (data.hadiah) {
    hadiahSection.style.display = "block";
    const hadiahList = document.getElementById('hadiahList');
    hadiahList.innerHTML = "";
    data.hadiah.forEach(h => {
      const li = document.createElement('li');
      li.className = 'detail-list-item';
      li.innerHTML = `<i class="ri-trophy-fill"></i> <span>${h}</span>`;
      hadiahList.appendChild(li);
    });
  } else {
    hadiahSection.style.display = "none";
  }
});
